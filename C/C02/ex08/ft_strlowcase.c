char	*ft_strlowcase(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			c = str[i];
			c += 32;
			str[i] = (char)c;
		}
		i++;
	}
	return (str);
}
