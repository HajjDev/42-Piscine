int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_substract(int a, int b)
{
	return (a - b);
}

int	ft_mul(int a, int b)
{
	return (a * b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_modulo(int a, int b)
{
	return (a % b);
}
