char	*ft_strupcase(char *str)
{
	int	i;
	int	c;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			c = str[i];
			c -= 32;
			str[i] = (char)c;
		}
		i ++;
	}
	return (str);
}
